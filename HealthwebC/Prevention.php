        <p class="sectionTag text-big">
          How can We reduce my risk of getting Covid-19....
        </p>
        <p class="sectionsubTag text-small">
          There are many things you can do to keep yourself and your loved ones
          safe from COVID-19. Know your risks to lower risks. Follow these basic
          precautions:<br /><br />

          1)Follow local guidance: Check to see what national, regional and
          local authorities are advising so you have the most relevant
          information for where you are.<br />
          2)Keep your distance: Stay at least 1 metre away from others, even if
          they don’t appear to be sick, since people can have the virus without
          having symptoms.<br />
          3)Wear a mask: Wear a well-fitting three-layer mask, especially when
          you can’t physically distance, or if you’re indoors. Clean your hands
          before putting on and taking off a mask.<br />
          4)Read our Masks and COVID-19 Q&A and watch our videos on how to wear
          and make masks.<br />
          5)Avoid crowded places, poorly ventilated, indoor locations and avoid
          prolonged contact with others. Spend more time outdoors than
          indoors.<br />
          6)Ventilation is important: Open windows when indoors to increase the
          amount of outdoor air.<br />
          7)Avoid touching surfaces, especially in public settings or health
          facilities, in case people infected with COVID-19 have touched them.
          Clean surfaces regularly with standard disinfectants.<br />
          8)Frequently clean your hands with soap and water, or an alcohol-based
          hand rub. If you can, carry alcohol-based rub with you and use it
          often.<br />
          9)Cover your coughs and sneezes with a bent elbow or tissue, throwing
          used tissues into a closed bin right away. Then wash your hands or use
          an alcohol-based hand rub.<br />
          10)Get vaccinated: When it’s your turn, get vaccinated. Follow local
          guidance and recommendations about vaccination.
        </p>
