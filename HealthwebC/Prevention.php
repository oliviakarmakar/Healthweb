          How does Covid-19 spread between two people.....
        </p>
        <p class="sectionsubTag text-small">
          We know that the disease is caused by the SARS-CoV-2 virus, which
          spreads between people in several different ways.<br /><br />

          Current evidence suggests that the virus spreads mainly between people
          who are in close contact with each other, for example at a
          conversational distance. The virus can spread from an infected
          person’s mouth or nose in small liquid particles when they cough,
          sneeze, speak, sing or breathe. Another person can then contract the
          virus when infectious particles that pass through the air are inhaled
          at short range (this is often called short-range aerosol or
          short-range airborne transmission) or if infectious particles come
          into direct contact with the eyes, nose, or mouth (droplet
          transmission).<br /><br />

          The virus can also spread in poorly ventilated and/or crowded indoor
          settings, where people tend to spend longer periods of time. This is
          because aerosols can remain suspended in the air or travel farther
          than conversational distance (this is often called long-range aerosol
          or long-range airborne transmission).<br /><br />

          People may also become infected when touching their eyes, nose or
          mouth after touching surfaces or objects that have been contaminated
          by the virus. <br /><br />
          Further research is ongoing to better understand the spread of the
          virus and which settings are most risky and why. Research is also
          under way to study virus variants that are emerging and why some are
          more transmissible. For updated information on SARS-CoV-2 variants,
          please read the weekly epidemiologic updates.
        </p>
