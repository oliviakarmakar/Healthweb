          Are there certain settings where Covid-19 can spread more easily?....
        </p>
        <p class="sectionsubTag text-small">
          Yes, any situation in which people are in close proximity to one
          another for long periods of time increases the risk of transmission.
          Indoor locations, especially settings where there is poor ventilation,
          are riskier than outdoor locations. Activities where more particles
          are expelled from the mouth, such as singing or breathing heavily
          during exercise, also increase the risk of transmission.<br /><br />

          The “Three C’s” are a useful way to think about this. They describe
          settings where transmission of the COVID-19 virus spreads more
          easily:<br /><br />

          1) Crowded places;<br />
          2)Close-contact settings, especially where people have conversations
          very near each other;<br />
          3)Confined and enclosed spaces with poor ventilation.<br /><br />
          The risk of COVID-19 spreading is especially high in places where
          these “3Cs” overlap.
        </p>
