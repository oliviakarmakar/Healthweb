            <p class="sectionTag text-big">How Variants Are Classified</p>
            <p class="sectionsubTag text-small">The U.S. Department of Health and Human Services (HHS) established a SARS-CoV-2 Interagency Group (SIG) to enhance coordination among CDC, National Institutes of Health (NIH), Food and Drug Administration (FDA), Biomedical Advanced Research and Development Authority (BARDA), and Department of Defense (DoD). This interagency group is focused on the rapid characterization of emerging variants and actively monitors their potential impact on critical SARS-CoV-2 countermeasures, including vaccines, therapeutics, and diagnostics.<br><br>

                The SIG meets regularly to evaluate the risk posed by SARS-CoV-2 variants circulating in the United States and to make recommendations about the classification of variants. This evaluation is undertaken by a group of subject matter experts who assess available data, including variant proportions at the national and regional levels and the potential or known impact of the constellation of mutations on the effectiveness of medical countermeasures, severity of disease, and ability to spread from person to person. Given the continuous evolution of SARS-CoV-2 and our understanding of the impact of variants on public health, variants may be reclassified based on their attributes and prevalence in the United States.<br><br>
                <h2>Notes:</h2> Each variant classification includes the possible attributes of lower classes (for example, VOC includes the possible attributes of VOI); variant status might escalate or deescalate based on emerging scientific evidence. This page will be updated as needed to show the variants that belong to each class. The World Health Organization (WHO) external icon also classifies variant viruses as variants of concern and variants of interest; U.S. classifications may differ from those of WHO because the impact of variants may differ by location. To assist with public discussions of variants, WHO proposed using labels consisting of the Greek alphabet (for example, alpha, beta, gamma) as a practical way to discuss variants for non-scientific audiences. The labels assigned to each variant are provided in the tables below.<br><br><br></p>
