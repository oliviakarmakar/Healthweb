            <p class="sectionsubTag text-small">If you think about a virus like a tree growing and branching out; each branch on the tree is slightly different than the others. By comparing the branches, scientists can label them according to the differences. These small differences, or variants, have been studied and identified since the beginning of the pandemic.<br><br>

                Some variations allow the virus to spread more easily or make it resistant to treatments or vaccines. Those variants must be monitored more carefully.<br><br>
                As the virus spreads, it has new opportunities to change and may become more difficult to stop. These changes can be monitored by comparing differences in physical traits (such as resistance to treatment) or changes in genetic code (mutations) from one variant to another.<br><br><br></p>
