            <p class="sectionTag text-big">Variant of Interest (VOI)</p>
            <p class="sectionsubTag text-small">A variant with specific genetic markers that have been associated with changes to receptor binding, reduced neutralization by antibodies generated against previous infection or vaccination, reduced efficacy of treatments, potential diagnostic impact, or predicted increase in transmissibility or disease severity.<br>

                Possible attributes of a Variant of Interest:<br>
                
                1.Specific genetic markers that are predicted to affect transmission, diagnostics, therapeutics, or immune escape.<br>
                2.Evidence that it is the cause of an increased proportion of cases or unique outbreak clusters.<br>
                3.Limited prevalence or expansion in the US or in other countries.<br>
                A Variant of Interest might require one or more appropriate public health actions, including enhanced sequence surveillance, enhanced laboratory characterization, or epidemiological investigations to assess how easily the virus spreads to others, the severity of disease, the efficacy of therapeutics and whether currently approved or authorized vaccines offer protection.<br><br><br></p>
