        <p class="sectionTag text-big">Why do we need to stay Fit?</p>
        <p class="sectionsubTag text-small">
          You already know there are many great reasons to exercise—from
          improving energy, mood, sleep, and health to reducing anxiety, stress,
          and depression. And detailed exercise instructions and workout plans
          are just a click away. But if knowing how and why to exercise was
          enough, we’d all be in shape. Making exercise a habit takes more—you
          need the right mindset and a smart approach.<br /><br /><br />
        </p>
