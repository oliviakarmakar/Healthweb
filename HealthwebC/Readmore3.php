    <section class="section1">
      <div class="paras">
        <p class="sectionTag text-big">How much exercise do you need?</p>
        <p class="sectionsubTag text-small">
          The key thing to remember about starting an exercise program is that
          something is always better than nothing. Going for a quick walk is
          better than sitting on the couch; one minute of activity will help you
          lose more weight than no activity at all. That said, the current
          recommendations for most adults is to reach at least 150 minutes of
          moderate activity per week. You’ll get there by exercising for 30
          minutes, 5 times a week. Can’t find 30 minutes in your busy schedule?
          It’s okay to break things up. Two 15-minute workouts or three
          10-minute workouts can be just as effective.<br /><br /><br />
        </p>
      </div>
    </section>

    <section class="section2">
      <div class="paras">
        <p class="sectionTag text-big">How hard do we need to exercise?</p>
        <p class="sectionsubTag text-small">
          Whether an activity is low, moderate, or vigorous intensity varies
          according to your personal fitness level. As a general guideline,
          though:<br />

          <br /><strong>Low-intensity activity:</strong> You can easily talk in
          full sentences, or sing.<br />
          <strong>Moderate intensity:</strong> You can speak in full sentences,
          but not sing.<br />
          <strong>Vigorous intensity:</strong> You are too breathless to speak
          in full sentences.<br />
          <br />For most people, aiming for moderate intensity exercise is
          sufficient to improve your overall health. You should breathe a little
          heavier than normal, but not be out of breath. Your body should feel
          warmer as you move, but not overheated or sweating profusely. While
          everyone is different, don’t assume that training for a marathon is
          better than training for a 5K or 10K. There’s no need to overdo it.<br /><br /><br />
        </p>
      </div>
    </section>

    <section class="section3">
      <div class="paras">
        <p class="sectionTag text-big">
          How to make exercise a habit that sticks
        </p>
        <p class="sectionsubTag text-small">
          There’s a reason so many New Year’s resolutions to get in shape crash
          and burn before February rolls around. And it’s not that you simply
          don’t have what it takes. Science shows us that there’s a right way to
          build habits that last. Follow these steps to make exercise one of
          them.

          <br /><br /><u>Start small and build momentum</u><br />
          A goal of exercising for 30 minutes a day, 5 times a week may sound
          good. But how likely are you to follow through? The more ambitious
          your goal, the more likely you are to fail, feel bad about it, and
          give up. It’s better to start with easy exercise goals you know you
          can achieve. As you meet them, you’ll build self-confidence and
          momentum. Then you can move on to more challenging goals.

          <br /><br /><u>Make it automatic with triggers</u><br />
          Triggers are one of the secrets to success when it comes to forming an
          exercise habit. In fact, research shows that the most consistent
          exercisers rely on them. Triggers are simply reminders—a time of day,
          place, or cue—that kick off an automatic reaction. They put your
          routine on autopilot, so there’s nothing to think about or decide on.
          The alarm clock goes off and you’re out the door for your walk. You
          leave work for the day and head straight to the gym. You spot your
          sneakers right by the bed and you’re up and running. Find ways to
          build them into your day to make exercise a no-brainer.
          <br /><br /><br />
        </p>
      </div>
    </section>

    <section class="section4">
      <div class="paras">
        <p class="sectionTag text-big">Overcoming obstacles to exercising</p>
        <p class="sectionsubTag text-small">
          Whatever your age or fitness level—even if you’ve never exercised a
          day in your life —there are steps you can take to make exercise less
          intimidating and painful and more fun and instinctive.

          <br /><br /><strong>Ditch the all-or-nothing attitude.</strong><br />
          You don’t have to spend hours in a gym or force yourself into
          monotonous or painful activities you hate to experience the physical
          and emotional benefits of exercise. A little exercise is better than
          nothing. In fact, adding just modest amounts of physical activity to
          your weekly routine can have a profound effect on your mental and
          emotional health.

          <br /><br /><strong>Be kind to yourself.</strong><br />
          Research shows that self-compassion increases the likelihood that
          you’ll succeed in any given endeavor. So, don’t beat yourself up about
          your body, your current fitness level, or your supposed lack of
          willpower. All that will do is demotivate you. Instead, look at your
          past mistakes and unhealthy choices as opportunities to learn and
          grow.

          <br /><br /><strong>Check your expectations.</strong><br />
          You didn’t get out of shape overnight, and you’re not going to
          instantly transform your body either. Expecting too much, too soon
          only leads to frustration. Try not to be discouraged by what you can’t
          accomplish or how far you have to go to reach your fitness goals.
          Instead of obsessing over results, focus on consistency. While the
          improvements in mood and energy levels may happen quickly, the
          physical payoff will come in time.<br /><br /><br />
        </p>
      </div>
    </section>

    <section class="section5">
      <div class="paras">
        <p class="sectionTag text-big">How to stay motivated to exercise</p>
        <p class="sectionsubTag text-medium">
          <li>
            No matter how much you enjoy an exercise routine, you may find that
            you eventually lose interest in it. That’s the time to shake things
            up and try something new or alter the way you pursue the exercises
            that have worked so far.
          </li>

          <br /><br />
          <li>
            Pair your workout with a treat. For example, you can listen to an
            audiobook or watch your favorite TV show while on the treadmill or
            stationary bike.
          </li>

          <br /><br />
          <li>
            Log your activity. Keep a record of your workouts and fitness
            progress. Writing things down or tracking them on an app increases
            commitment and holds you accountable to your routine. Later on, it
            will also be encouraging to look back at where you began.
          </li>

          <br /><br />
          <li>
            Harness the power of the community. Having others rooting for us and
            supporting us through exercise ups and downs helps to keep
            motivation strong. There are numerous online fitness communities you
            can join. You can also try working out with friends either in person
            or remotely using fitness apps that let you track and compare your
            progress with each other.
          </li>

          <br /><br />
          <li>
            Get inspired. Read a health and fitness magazine or visit an
            exercise website and get inspired with photos of people being
            active. Sometimes reading about and looking at images of people who
            are healthy and fit can motivate you to move your body.
          </li>
          <br /><br /><br />
        </p>
